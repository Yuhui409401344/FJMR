            管理與政策、資訊管理、行銷管理、會計、審計、生產與作業管理、統計、數量方法、人力資源管理、國際企業、國際貿易、財務管理、管理經濟、產業經濟<br><br>
            四. 本刊為學術性期刊，自2005年起一年三期，於每年一月、五月及九月出版。同時為響應節能減碳，自第17卷起已不再接受紙版訂閱，且自第18卷起已停止紙版印刷。本刊所有已刊登之文章皆已彙整於本院網站，歡迎上網查閱相關資訊。<br><br>
