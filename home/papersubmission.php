            六. 文字：稿件應以中文或英文書寫，並各附300字以內之中文及英文摘要，以及五個以下關鍵字。<br><br>
            七. 打字、編排：中英文文稿皆須電腦打字，所存之文字檔（含圖表），必須為Microsoft Word 軟體可讀取(For IBM PC)。 <br><br>
            八. 來稿請將檔案寄送到本刊公用電子郵件信箱（請準備2份pdf檔案，其中1份載明作者相關資料，另一份無須載明）。寄送後懇請以電話確認。<br><br>
            九. 文長：文（含圖表）以10,000～12,000字左右為原則。<br><br>
            十. 來稿一經刊登，文責由作者自負，並同意本刊為發行紙版及網路電子期刊所必要之重製、散佈及公開傳輸；除作者自行出版外，本刊嚴禁一稿多投。</p><br>
