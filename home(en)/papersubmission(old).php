    <p style="text-indent: 2.5em;">The publication of an article in a peer-reviewed journal is an essential building block in the development of a coherent and respected network of knowledge. It is a direct reflection of the quality of work of the author and the institutions that support them. Peer-reviewed articles support and embody the scientific method. It is therefore important to agree upon standards of expected ethical behavior.</p>
                <br>
                Ethics topics to consider when publishing:<br>
                <br>
                <ul>
                <li style="list-style-type:None;"><b>Authorship of the paper:</b></li>
                <ol>Authorship should be limited to those who have made a significant contribution to the conception, design, execution, or interpretation of the reported study. Transparency about the contributions of authors is encouraged.
                </ol>
                <li style="list-style-type:None;"><b>Originality and plagiarism:</b></li>
                <ol>The authors should ensure that they have written entirely original works, and if the authors have used the work and/or words of others, that this has been appropriately cited or quoted.
                </ol>
                <li style="list-style-type:None;"><b>Multiple, redundant or concurrent publication:</b></li>
                <ol>An author should not in general publish manuscripts describing essentially the same research in more than one journal or primary publication.
                </ol>
                <li style="list-style-type:None;"><b>Acknowledgement of sources:</b></li>
                <ol>Proper acknowledgment of the work of others must always be given.
                </ol>
                <li style="list-style-type:None;"><b>Disclosure and conflicts of interest:</b></li>
                <ol>All submissions must include disclosure of all relationships that could be viewed as presenting a potential conflict of interest.
                </ol>
                <li style="list-style-type:None;"><b>Use of patient images or case details:</b></li>
                <ol>Studies on patients or volunteers require ethics committee approval and informed consent, which should be documented in the paper.
                </ol>
                </ul>
