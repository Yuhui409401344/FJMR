&nbsp; &nbsp;Fu Jen Management Review (FJMR) is an academic journal affiliated by the college of Management in Fu Jen Catholic University. FJMR is a double-blind
peer-reviewed journal that welcomes empirical, conceptual and methodological contributions on any topics relevant to the broad disciplines of business. 
However, FJMR is particularly concerned with business, information management, technologies, accounting, finance, and statistics. 

FJMR welcomes research that applies a broad array of approaches and epistemologies, including any mix of qualitative, quantitative, mixed-methods, action,
participatory, evaluation, design, development or other established methodologies. FJMR publishes three issues a year. Two of them are regular issues, and the
other one is specific issue of social enterprise.</br>

